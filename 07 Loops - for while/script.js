// Loops are powerful tools for performing repetitive tasks efficiently. Loops in JavaScript execute a block of code again and again while the condition is true.


// for loop

for (let i = 0; i < 10; i++) {
    console.log(i);
}


console.log("\n");

// while loop

let i=0;
while (i<10) {
    console.log(i);
    i++;
}